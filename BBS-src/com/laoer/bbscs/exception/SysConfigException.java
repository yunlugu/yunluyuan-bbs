package com.laoer.bbscs.exception;

public class SysConfigException extends Exception {

	/**
	 *
	 */
	private static final long serialVersionUID = -2297357466885586497L;

	public SysConfigException() {
		super();
	}

	public SysConfigException(String message) {
		super(message);
	}

	public SysConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public SysConfigException(Throwable cause) {
		super(cause);
	}
}
